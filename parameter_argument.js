// 1️⃣ Parameter
// A parameter is a placeholder (variable) defined in the function declaration. It acts as a variable that will receive a value when the function is called.

function greet(name) {  // 'name' is a parameter
  console.log(`Hello, ${name}!`);
}


// 2️⃣ Argument

// An argument is the actual value passed to a function when calling it. This value replaces the parameter inside the function.

greet("Alice"); // "Alice" is an argument
